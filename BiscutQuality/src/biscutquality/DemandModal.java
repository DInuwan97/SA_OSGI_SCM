package biscutquality;

public class DemandModal {
	private int demandReqId;
	private String productDetails;
	private String demandReason;
	private int salesMsgId;
	private String reqDate;
	private String approvalState;
	
	
	public int getDemandReqId() {
		return demandReqId;
	}
	public void setDemandReqId(int demandReqId) {
		this.demandReqId = demandReqId;
	}
	public String getProductDetails() {
		return productDetails;
	}
	public void setProductDetails(String productDetails) {
		this.productDetails = productDetails;
	}
	public String getDemandReason() {
		return demandReason;
	}
	public void setDemandReason(String demandReason) {
		this.demandReason = demandReason;
	}
	public int getSalesMsgId() {
		return salesMsgId;
	}
	public void setSalesMsgId(int salesMsgId) {
		this.salesMsgId = salesMsgId;
	}
	public String getReqDate() {
		return reqDate;
	}
	public void setReqDate(String reqDate) {
		this.reqDate = reqDate;
	}
	public String isApprovalState() {
		return approvalState;
	}
	public void setApprovalState(String approvalState) {
		this.approvalState = approvalState;
	}
	
	
	
}
