package tranportationpublisher;

public class TransportationVehicleModal {
	private int vehicleid,driverID;
	private String vehicelPlate,driverName;
	
	public int getVehicleid() {
		return vehicleid;
	}
	public void setVehicleid(int vehicleid) {
		this.vehicleid = vehicleid;
	}
	public int getDriverID() {
		return driverID;
	}
	public void setDriverID(int driverID) {
		this.driverID = driverID;
	}
	public String getVehicelPlate() {
		return vehicelPlate;
	}
	public void setVehicelPlate(String vehicelPlate) {
		this.vehicelPlate = vehicelPlate;
	}
	public String getDriverName() {
		return driverName;
	}
	public void setDriverName(String driverName) {
		this.driverName = driverName;
	}
	
	
}
