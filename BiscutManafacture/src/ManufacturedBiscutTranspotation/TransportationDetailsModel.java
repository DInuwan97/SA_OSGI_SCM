package ManufacturedBiscutTranspotation;

public class TransportationDetailsModel {

	private String vehicleType;
	private String vehicleNumber;
	private String driverName;
	private String cleanerName;
	private String packedDate;
	private String packedTime;
	private String esmitedTimeToDestination;
	
	
	
	public String getVehicleType() {
		return vehicleType;
	}
	public void setVehicleType(String vehicleType) {
		this.vehicleType = vehicleType;
	}
	public String getVehicleNumber() {
		return vehicleNumber;
	}
	public void setVehicleNumber(String vehicleNumber) {
		this.vehicleNumber = vehicleNumber;
	}
	public String getDriverName() {
		return driverName;
	}
	public void setDriverName(String driverName) {
		this.driverName = driverName;
	}
	public String getCleanerName() {
		return cleanerName;
	}
	public void setCleanerName(String cleanerName) {
		this.cleanerName = cleanerName;
	}
	public String getPackedDate() {
		return packedDate;
	}
	public void setPackedDate(String packedDate) {
		this.packedDate = packedDate;
	}
	public String getPackedTime() {
		return packedTime;
	}
	public void setPackedTime(String packedTime) {
		this.packedTime = packedTime;
	}
	public String getEsmitedTimeToDestination() {
		return esmitedTimeToDestination;
	}
	public void setEsmitedTimeToDestination(String esmitedTimeToDestination) {
		this.esmitedTimeToDestination = esmitedTimeToDestination;
	}
	
	
	
}
